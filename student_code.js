kiwi_caught = 0;


function process_frame() {
	if(is_down("b") == true){
		brake_truck();
		compute_truck_position();
	}
	else if(is_down("left") == true){
		translate_truck_left();
		compute_truck_position();
	}
	else if(is_down("right") == true){
		translate_truck_right()
		compute_truck_position();
	} 
	else{
		coast_truck();
		compute_truck_position();
	}
	if ((Math.random()*100) <= kiwi_rate){
		create_kiwi();
	}
}


function translate_truck_right() {
	var velocity = get_truck_velocity();
    velocity += 1; 
    if (velocity > 20) {
        velocity = 20;
    }
    set_truck_velocity(velocity);
}

function translate_truck_left() {
	var velocity = get_truck_velocity();
    velocity -= 1; 
    if (velocity < -20) {
        velocity = -20;
    }
    set_truck_velocity(velocity);
}

function coast_truck() {
	var velocity = get_truck_velocity(),
	stop = 0;
	decel = 1
    if (velocity > stop){
        velocity -= decel;
    }
    else if (velocity < stop){
        velocity += decel;
    }
    else{
        velocity = stop;
    }
    set_truck_velocity(velocity);
}

function brake_truck() {
	var velocity = get_truck_velocity(),
	stop = 0;
	decel = 2
    if (velocity > stop){
        velocity -= decel;
    }
    else if (velocity < stop){
        velocity += decel;
    }
    else{
        velocity = stop;
    }
    set_truck_velocity(velocity);
}

function compute_truck_position() {
	var old = get_truck_left();
    var velocity = get_truck_velocity();
    set_truck_left(old + velocity);
    var old1 = get_truck_left();
    if (old1<0) {
        velocity = 5 ;
        set_truck_velocity(velocity);
        set_truck_left(old1 +velocity)
    }
    set_truck_left(old + velocity);
    if (old1+250>window.innerWidth){
    	velocity = -5;
    	set_truck_velocity(velocity);
    	set_truck_left(old1 +velocity);
    }
}

function find_collisions(kiwi) {
	var kix = get_kiwi_x(kiwi);
    var kiy = get_kiwi_y(kiwi);
    var truckx = get_truck_left();
    if (kix>truckx && kix<(truckx+250) && (window.innerHeight-kiy)<17) {
    	//counts if kiwi touches the truck 
    	delete_kiwi(kiwi);
    	kiwi_caught += 1; 
    	document.getElementById("kiwi-count").innerHTML = kiwi_caught;
    }
    
}

function game_over() {
	title= "Game Over!";
	text= "Congratulations! Kiwis caught: "+kiwi_caught;
	button= "Play again!";
	alert(title,text,button);
	kiwi_caught = 0;
	document.getElementById("kiwi-count").innerHTML = kiwi_caught;
}

